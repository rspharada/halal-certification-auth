//! utils.rs
//!
//! アプリケーション全体で再利用可能なユーティリティ関数を定義するファイルです。
//!
//! ✅ 現在の用途：
//! - 入力バリデーション関数（例：メール形式の検証、空文字チェック）
//! - 日付・文字列などの変換関数
//! - ログ出力やデバッグ用ヘルパー
//!
//! 🧭 将来的な用途：
//! - 複数ページ間で使われる汎用ロジックの共通化
//! - APIエラー処理の標準化（例：レスポンスからエラーメッセージを抽出）
//! - トークン／クッキー操作などのブラウザサイド関数
//!
//! ※ ページ固有・画面固有の処理はそれぞれのファイルに実装し、
//!    汎用性が高まった段階で本ファイルに抽出してください。
